                        <div class="timeline-item">
                            <div class="timeline-year">2004</div>
                            <div class="timeline-content">
                                <p>En los últimos meses de 2004, Monseñor José Oscar Barahona Castillo, segundo Obispo de la Diócesis de San Vicente, decidió trasladar a los seminaristas de teología del Seminario Mayor "Nuestra Señora del Camino" (Sololá, Guatemala) al Seminario Menor "Pío XII" en San Vicente. Su propósito era estar más cerca del proceso formativo de los futuros sacerdotes, considerando que se contaba con lo necesario para iniciar esta obra diocesana.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2005</div>
                            <div class="timeline-content">
                                <p>Se inaugura el cuatrienio teológico en las instalaciones del Seminario Menor "Pío XII". El rector, padre William Flores, ya ejercía funciones y contaba con el apoyo de los diáconos de ese momento. Así comienza el primer año lectivo del actual Seminario Mayor de "La Inmaculada", marcado por cierta inestabilidad propia de sus inicios.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2006</div>
                            <div class="timeline-content">
                                <p>Con la llegada del nuevo Obispo, Monseñor José Luis Escobar (actual arzobispo de San Salvador), se nombra rector al padre Adán Ramos y se confirma como vicerrector al padre Enrique Barrera. Este año se caracteriza por la implementación de la disciplina y el establecimiento de las bases del nuevo centro de formación sacerdotal, considerado como la primera etapa formativa del seminario.</p>
                            </div>
                        </div>
                        
                        <!-- Continuar con los demás años de la historia -->
                        <div class="timeline-item">
                            <div class="timeline-year">2007</div>
                            <div class="timeline-content">
                                <p>Se continúa con la línea trazada en el año anterior. El padre Enrique Barrera es nombrado rector del Seminario Menor "Pío XII", que regresa a sus instalaciones originales. De esta forma, se consolidan dos seminarios en la ciudad de San Vicente: el teologado de "La Inmaculada" y el Seminario Menor "Pío XII". El padre Eliseo Rivas asume como vicerrector, y en octubre se incorpora el padre Gustavo Romero en ese mismo cargo.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2008</div>
                            <div class="timeline-content">
                                <p>Monseñor José Luis Escobar decide trasladar a los estudiantes de filosofía del Seminario "Juan XXIII" de Santa Ana. Con esta integración, el seminario adquiere oficialmente el nombre de Seminario Mayor de "La Inmaculada", al contar con todos los niveles formativos: desde el año propedéutico hasta el cuarto año de teología. Dos sacerdotes continúan al frente de la institución.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2009</div>
                            <div class="timeline-content">
                                <p>Este año transcurre sin mayores novedades, salvo el fortalecimiento del proceso formativo y la incorporación del padre Constantino Barrera (actual obispo de Sonsonate) durante los últimos dos meses del segundo ciclo.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2010</div>
                            <div class="timeline-content">
                                <p>Monseñor Elías Bolaños, administrador apostólico de la Diócesis de San Vicente y Obispo de Zacatecoluca, suma al padre Gerardo Amado al equipo formador. En el segundo ciclo se une también el padre Eduardo Chávez. En junio y julio, los padres Eduardo y Gustavo participan en el curso internacional para formadores de seminarios en Leggiuno, Italia, con el objetivo de fortalecer el rol del formador y el proceso formativo. En noviembre, se realiza el primer envío misionero de seminaristas al Vicariato Apostólico de Bluefields, como parte del compromiso pastoral y misionero.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2011</div>
                            <div class="timeline-content">
                                <p>Se considera el inicio de la segunda etapa formativa. El padre Eduardo Chávez es nombrado rector y, tras el curso internacional, se inicia un proceso de reestructuración del plan formativo. Este período representa una transición entre etapas, cuyos efectos se sienten en toda la comunidad.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2012</div>
                            <div class="timeline-content">
                                <p>Se incorpora al equipo formador el padre Remberto Huezo. Ese año se envía la <em>ratio formationis</em> a Roma para su revisión y aprobación. Se reorganiza el aspecto académico, logrando que los profesores del área teológica (sacerdotes y laicos) cuenten con especialización. Todas las áreas y comisiones del seminario se fortalecen, adquiriendo mayor formalidad institucional.</p>
                            </div>
                        </div>
                        
                        <div class="timeline-item">
                            <div class="timeline-year">2013</div>
                            <div class="timeline-content">
                                <p>En el noveno año lectivo, se logra consolidar un equipo de profesores especializados también en el área filosófica. Por motivos externos, un formador deja la institución y otro se prepara para estudios en Roma. El seminario cuenta temporalmente con dos formadores, en espera de nuevas incorporaciones. Esto abre la posibilidad de renovar completamente el equipo formador, marcando el inicio de una tercera etapa en la historia del seminario.</p>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </section>

